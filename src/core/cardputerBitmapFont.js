export function cardputerBitmapScale(fontSize = 12) {
  return Math.max(1, Math.round(Number(fontSize || 12) / CARDPUTER_BITMAP_FONT_5X7.lineHeight));
}

export function cardputerBitmapTextWidth(text = '', fontSize = 12) {
  return [...String(text)].length * CARDPUTER_BITMAP_FONT_5X7.glyphAdvance * cardputerBitmapScale(fontSize);
}

export function cardputerBitmapTextHeight(fontSize = 12) {
  return CARDPUTER_BITMAP_FONT_5X7.lineHeight * cardputerBitmapScale(fontSize);
}

export function cardputerBitmapGlyph(char) {
  const codepoint = String(char || '?').codePointAt(0) ?? 0x3f;
  const normalized = codepoint < CARDPUTER_BITMAP_FONT_5X7.firstCodepoint || codepoint > CARDPUTER_BITMAP_FONT_5X7.lastCodepoint ? 0x3f : codepoint;
  return CARDPUTER_BITMAP_FONT_5X7.glyphs[normalized - CARDPUTER_BITMAP_FONT_5X7.firstCodepoint];
}

export function drawCardputerBitmapTextSvg(parent, svgFactory, { text = '', x = 0, y = 0, color = '#ffffff', fontSize = 12, align = 'left', firmwareCenter = false } = {}) {
  const scale = cardputerBitmapScale(fontSize);
  const width = cardputerBitmapTextWidth(text, fontSize);
  let startX = Math.round(x);
  let topY = Math.round(y);

  if (align === 'center') {
    startX -= Math.trunc(width / 2);
    if (firmwareCenter) topY -= Math.trunc((CARDPUTER_BITMAP_FONT_5X7.glyphHeight * scale) / 2);
  } else if (align === 'right') {
    startX -= width;
  }

  const group = svgFactory('g', {
    class: 'bitmap-text',
    'data-font': CARDPUTER_BITMAP_FONT_5X7.id,
    'shape-rendering': 'crispEdges'
  });
  [...String(text)].forEach((char, index) => {
    const glyph = cardputerBitmapGlyph(char);
    const glyphX = startX + index * CARDPUTER_BITMAP_FONT_5X7.glyphAdvance * scale;
    for (let col = 0; col < CARDPUTER_BITMAP_FONT_5X7.glyphWidth; col += 1) {
      const bits = glyph[col];
      for (let row = 0; row < CARDPUTER_BITMAP_FONT_5X7.glyphHeight; row += 1) {
        if (bits & (1 << row)) {
          group.append(svgFactory('rect', {
            x: glyphX + col * scale,
            y: topY + row * scale,
            width: scale,
            height: scale,
            fill: color
          }));
        }
      }
    }
  });
  parent.append(group);
  return group;
}
